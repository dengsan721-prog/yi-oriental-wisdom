function chartComparison(specific: string) {
  return `${specific}。当太阳星座描述与现实行为不一致时，不要硬套；八字使用出生时刻的干支时间坐标，两套文化框架不混合计算。完整西方星盘还需出生时间与地点，并综合月亮、上升、行星、宫位和相位。`;
}

function makeProfile(
  sign: ZodiacSign,
  element: ZodiacProfile["element"],
  modality: ZodiacProfile["modality"],
  copy: ProfileCopy,
): ZodiacProfile {
  const expanded = Object.fromEntries(Object.entries(copy).map(([field, text]) => [
    field,
    field === "chartComparison" ? chartComparison(text) : `${text}。${FIELD_GUIDES[field as keyof typeof FIELD_GUIDES]}`,
  ])) as ProfileCopy;

  return {
    sign,
    element,
    modality,
    ...expanded,
    caution: CAUTION,
    sourceReferences: ["culture.nasa-constellations"],
  };
}

