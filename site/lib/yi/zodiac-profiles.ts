function makeProfile(
  sign: ZodiacSign,
  element: ZodiacProfile["element"],
  modality: ZodiacProfile["modality"],
  copy: ProfileCopy,
): ZodiacProfile {
  return {
    sign,
    element,
    modality,
    ...copy,
    caution: CAUTION,
    sourceReferences: ["culture.nasa-constellations"],
  };
}

